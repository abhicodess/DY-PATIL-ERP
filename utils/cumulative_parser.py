def _safe_int(val):
    try:
        s = str(val).strip()
        return int(float(s)) if s else 0
    except Exception:
        return 0

def _safe_float(val):
    try:
        return round(float(str(val).strip()), 2)
    except Exception:
        return 0.0

def _detect_meta(text: str) -> dict:
    meta = {"year": "", "semester": "", "program": "", "department": "", "division": ""}
    m = re.search(r"Academic Year ([\d\-]+),\s*Semester\s*-\s*(\w+)", text, re.IGNORECASE)
    if m:
        meta["year"] = m.group(1).strip()
        meta["semester"] = m.group(2).strip()
    m = re.search(r"Program:\s*(.+)", text)
    if m:
        prog = m.group(1).strip()
        meta["program"] = prog
        div_m = re.search(r"Div\.?\s*([A-Z])", prog)
        meta["division"] = div_m.group(1) if div_m else ""
        prog_up = prog.upper()
        if "AIML" in prog_up:
            meta["department"] = "AIML"
        elif "AIDS" in prog_up:
            meta["department"] = "AIDS"
        elif "IT" in prog_up and "COMP" not in prog_up:
            meta["department"] = "IT"
        else:
            meta["department"] = "CS"
    return meta


def parse_pdf(filepath: str) -> dict:
    """
    Parse a DY Patil attendance PDF.
    Returns:
      {
        meta: {year, semester, program, department, division},
        subjects: [{name, code, conducted}],
        students: [{roll, name, total, pct, attended:[int,...]}]
      }
    """
    try:
        import pdfplumber
    except ImportError:
        raise RuntimeError("pdfplumber not installed. Run: pip install pdfplumber")

    result = {"meta": {}, "subjects": [], "students": []}

    with pdfplumber.open(filepath) as pdf:
        first_text = pdf.pages[0].extract_text() or ""
        result["meta"] = _detect_meta(first_text)

        # Collect all table rows across all pages
        all_rows = []
        for page in pdf.pages:
            tables = page.extract_tables()
            if tables:
                all_rows.extend(tables[0])

        subj_names = []
        subj_codes = []
        conducted = []
        student_rows = []

        for row in all_rows:
            if not row or len(row) < 4:
                continue
            c0, c1, c2 = str(row[0] or ""), str(row[1] or ""), str(row[2] or "")

            if "TOTAL NO." in c2.upper() or "CONDUCTED" in c2.upper() or "TOTAL LECTURES" in c2.upper():
                # conducted counts in cols 3..-2
                conducted = row[3:-2]

            elif c0.strip() in ("", "None") and c2.strip() in ("", "None") and row[3] and "U24" in str(row[3]):
                # Subject codes row
                subj_codes = row[3:-2]

            elif "SR" in c0.upper() and "NO" in c0.upper():
                # Header row — subject names
                subj_names = row[3:-2]

            elif c0.strip().lstrip("0").isdigit() and c1.strip():
                # Student data row
                student_rows.append(row)

        result["subjects"] = []
        for i, name in enumerate(subj_names):
            result["subjects"].append({
                "name": (name or "").replace("\n", " ").strip(),
                "code": (subj_codes[i] if i < len(subj_codes) else "").replace("\n", "").strip(),
                "conducted": _safe_int(conducted[i]) if i < len(conducted) else 0,
            })

        for row in student_rows:
            if not row[1]:
                continue
            attended_raw = row[3:-2]
            result["students"].append({
                "roll": str(row[1]).strip(),
                "name": str(row[2] or "").strip(),
                "total": _safe_int(row[-2]),
                "pct": _safe_float(row[-1]),
                "attended": [_safe_int(x) for x in attended_raw],
            })

    return result


