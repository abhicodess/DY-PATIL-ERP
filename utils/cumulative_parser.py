def parse_attendance_file(filepath_or_bytes, filename: str):
    """Auto-detect format and parse."""
    fname = filename.lower()
    if fname.endswith(".pdf"):
        if isinstance(filepath_or_bytes, (bytes, bytearray)):
            import tempfile, os
            with tempfile.NamedTemporaryFile(delete=False, suffix=".pdf") as tmp:
                tmp.write(filepath_or_bytes)
                tmp_path = tmp.name
            try:
                return parse_pdf(tmp_path)
            finally:
                os.unlink(tmp_path)
        return parse_pdf(filepath_or_bytes)
    elif fname.endswith((".xlsx", ".xls")):
        return parse_excel(filepath_or_bytes, filename)
    else:
        raise ValueError(f"Unsupported file type: {filename}. Use .pdf or .xlsx")
